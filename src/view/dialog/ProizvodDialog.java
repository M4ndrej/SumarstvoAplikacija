    private void jButtonKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKreirajActionPerformed
        Tip tip = (Tip)jComboBoxTip.getSelectedItem();
        Vrsta vrsta = (Vrsta)jComboBoxVrsta.getSelectedItem();
        Klasa klasa = (Klasa)jComboBoxKlasa.getSelectedItem();
        double cena = Double.parseDouble(jTextFieldCena.getText());
        MernaJedinica mj = (MernaJedinica)jComboBoxMernaJedinica.getSelectedItem();
        String opis = jTextAreaOpis.getText();
        Proizvod proizvod = new Proizvod(0,tip, vrsta, klasa, opis, cena, mj);
        boolean uspesno = Controller.getInstance().kreirajProizvod(proizvod);
         if (uspesno) {
            JOptionPane.showMessageDialog(this, "Proizvod uspe≈°no izmenjen", "Obave≈°tenje", JOptionPane.INFORMATION_MESSAGE);
            parent.azurirajTabelu();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Gre≈°ka prilikom izmene proizvoda", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonKreirajActionPerformed

    private void jButtonSacuvajIzmeneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajIzmeneActionPerformed
        Tip tip = (Tip)jComboBoxTip.getSelectedItem();
        Vrsta vrsta = (Vrsta)jComboBoxVrsta.getSelectedItem();
        Klasa klasa = (Klasa)jComboBoxKlasa.getSelectedItem();
        MernaJedinica mj = (MernaJedinica)jComboBoxMernaJedinica.getSelectedItem();
        String opis = jTextAreaOpis.getText();
        double cena = Double.parseDouble(jTextFieldCena.getText());
        proizvod.setTip(tip);
        proizvod.setVrsta(vrsta);
        proizvod.setKlasa(klasa);
        proizvod.setMernaJedinica(mj);
        proizvod.setOpis(opis);
        proizvod.setCena(cena);
        boolean uspesno = Controller.getInstance().izmeniProizvod(proizvod);
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Proizvod uspe≈°no kreiran", "Obave≈°tenje", JOptionPane.INFORMATION_MESSAGE);
            parent.azurirajTabelu();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Gre≈°ka prilikom kreiranja proizvoda", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajIzmeneActionPerformed

    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da ≈æelite da obri≈°ete proizvod?","Potvrda",JOptionPane.YES_NO_OPTION);
        if(odgovor == JOptionPane.YES_OPTION){
            boolean uspesno = Controller.getInstance().orbisiProizvod(proizvod);
        if(!uspesno){
            JOptionPane.showMessageDialog(this, "Gre≈°ka prilikom brisanja proizvoda","Gre≈°ka",JOptionPane.ERROR_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Uspe≈°no obrisan proizvod","Uspe≈°no",JOptionPane.INFORMATION_MESSAGE);
            parent.azurirajTabelu();
            this.dispose();
        }
        }
        return;
    }//GEN-LAST:event_jButtonObrisiActionPerformed

